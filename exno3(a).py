# String Operators

s='im a good boy :)'
a='str'
b="ww"
print(type(s))
print(s.capitalize()) #capitalize the first letter
print(a.center(10,"f")) #adds the string in center of characters
print(a.ljust(10,"f"))#adds the string in left of characters
print(a.rjust(10,"f"))#adds the string in right of characters
print(a,"\t",b) #\t adds tab (white space)
print(a.expandtabs(8))#expand tabs increases the tab space
print(s.isnumeric())
print(s.isdigit)
print(s.isdecimal)
print(s.swapcase())#if the letter is notcapitslized it capitalizes vce versa
print(b.isupper())
print(b.islower())
print(len(a))
print(a.lstrip())
print(max(a))
print(min(s))#last character
print(s.rfind("a"))