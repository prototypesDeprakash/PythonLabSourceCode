#Write a program to find the diameter , circumference and area of a circle
#area = pi*r^2
#circumference = 2*pi*r
# diameter = 2*r

pi = 3.14
r = int(input("Enter the radius of the circle : "))
print("The area of the circle is  %s " %(pi*r**2))
print("The Circumference of the circle is %s" %(2*pi*r))
print("the diameter of the Circle is %s " %(2*r))